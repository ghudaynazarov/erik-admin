// Function to set language
function setLanguage(language) {
  const RU = document.getElementById('RU');
  const EN = document.getElementById('EN');
  const ruTitle = document.getElementById('ruTitle');
  const enTitle = document.getElementById('enTitle');

  if(language == 'en') {
    RU.style = 'display:none';
    EN.style = 'display:none'.remove;
    enTitle.style = 'display:none'.remove
    ruTitle.style = 'display:none';
  }
  if(language == 'ru') {
    EN.style = 'display:none';
    RU.style = 'display:none'.remove;
    ruTitle.style = 'display:none'.remove;
    enTitle.style = 'display:none';
  }

  var elements = document.querySelectorAll('[data-locale]');
  var placeholderElements = document.querySelectorAll('[data-locale-placeholder]');
  
  for (var i = 0; i < elements.length; i++) {
    var element = elements[i];
    var textKey = element.getAttribute('data-locale');
    element.innerText = languageData[language][textKey];
  }
  
  for (var j = 0; j < placeholderElements.length; j++) {
    var placeholderElement = placeholderElements[j];
    var placeholderKey = placeholderElement.getAttribute('data-locale-placeholder');
    placeholderElement.placeholder = languageData[language][placeholderKey];
  }
  
  // Store selected language in localStorage
  localStorage.setItem('selectedLanguage', language);
}

// Function to get the saved language from localStorage
function getSavedLanguage() {
  return localStorage.getItem('selectedLanguage');
}

// Check if a language is saved in localStorage, if not, use default language 'en'
var savedLanguage = getSavedLanguage();
var initialLanguage = savedLanguage ? savedLanguage : 'en';
setLanguage(initialLanguage); // Set initial language

// Event listener to change language when buttons are clicked
document.querySelectorAll('.language-button').forEach((button) => {
  button.addEventListener('click', function () {
    var language = this.getAttribute('data-language');
    setLanguage(language);
  });
});

